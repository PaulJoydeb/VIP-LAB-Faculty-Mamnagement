        <p class="py-3"> Computer science & Engineering (CSE) was established in 2002 which is a pioneer department of Stamford University Bangladesh. The CSE department has recently received the IEB accreditation and affiliations like Microsoft Imagine listed school. The mission of CSE department is to develop qualified and competent human resources with global standard teaching by ensuring up-to-date curriculum, classroom, lab facilities, and overall teaching quality improvement through training, workshops, conferences, industry collaboration etc., while conducting innovative research. The Department of Computer Science & Engineering maintains quality curriculums offering - i) B.Sc. in Computer Science & Engineering (CSE) ii) M.Sc. in Computer Science & Engineering (MCSE), and iii) Masters in Computer Applications (MCA). The graduates and post-graduates of the department are serving in many reputed IT companies at home and abroad. The Department of Computer Science & Engineering is regularly organizing seminars, workshops, and events such as Microsoft Technology Day, CSE Festivals, Cultural events, Annual Picnic etc. The students of the department are regularly participating in different programming contests, robotics competitions, debating etc. The department has established BASIS student forum, Stamford University Computer Society etc to strengthen IT industry collaborations. To enhance the IT skills in real-world scenario and to place themselves in tomorrow’s IT world; the department students are undergoing LICT-TopUp professional free of cost training courses on Android, Microsoft .NET, Web design etc by Earnest and Young. The strength of the department also lies in highly educated faculty members at home and abroad. At present, the department has twenty-two (23) full-time faculty members, an adjunct professor, and currently the department is chaired by Dr. Talal Rahman.

 

